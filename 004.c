#include <stdio.h>



/* A program that asks, reads and displays the name, age of a user.
 * Name is David
 * Age is 65
*/

int main()
{
	int name = David;
	int age = 65;

	printf("What is your name: \n");
	scanf("%d", name);

	printf("What is your age: \n");
	scanf("%d", age);

	printf("Your name is %d and you are %d years old");

	return 0;

}

